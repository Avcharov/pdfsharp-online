class ImageItem {
    constructor(id, name, xPos, yPos, rotation, opacity, isHidden, viewId) {
      this.id = id;
      this.name = name;
      this.xPos = xPos;
      this.yPos = yPos;
      this.rotation = rotation;
      this.opacity = opacity;
      this.isHidden = isHidden;
      this.viewId = viewId;
    }
  }

export default ImageItem;